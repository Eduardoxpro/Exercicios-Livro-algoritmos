#Escreva um programa que leia um valor em metros ea exiba convertido em centimetros e milimetros
METROS = int(input("Digite o valor em metros: "))
CENTIMETROS = METROS * 100
MILIMETROS = METROS * 1000
print ("O valor em Centimetros é: {} \n O valor em Milimetros é: {} " .format(CENTIMETROS, MILIMETROS))