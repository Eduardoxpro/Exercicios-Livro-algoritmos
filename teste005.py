# Faça um programa que leia um numero inteiro e mostre na tela seu sucesso e seu antecessor.

VAR = int(input("digite o valor que deseja sabe o sucesso e o antecessor: "))
SUCESSOR = VAR + 1
ANTECESSOR = VAR - 1
print ("O sucessor do valor é: {} o antecessor do valor é: {} " .format(SUCESSOR, ANTECESSOR))