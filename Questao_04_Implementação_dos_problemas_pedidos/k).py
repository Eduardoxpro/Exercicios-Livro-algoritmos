# k) Conversor Dólar → Real
print("k) CONVERSOR DÓLAR → REAL")
cotacao = float(input("Cotação do dólar hoje (R$): "))
dolares = float(input("Quantos dólares você tem? "))
reais = dolares * cotacao
print(f"Você tem R$ {reais:.2f}")
print("="*40)

